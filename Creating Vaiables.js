//=========================================== Varable with let Keyword================================================//

// Example 1

let myName = "abdul rauf"; // created a variable with string
console.log(myName); // output is abdul rauf

// Example 2

let myAge = 99; // created a variable withe number type
console.log(myAge); // output is 99

// Example 3

myName = "abdul rauf marth"; // reassigned a new value to variable
console.log(myName); // output is abdul rauf marth

// Example 4

myAge = 77; // reassigned a new value to variable
console.log(myAge); // output is 77

//=============================================== Varable with Const Keyword================================================//

